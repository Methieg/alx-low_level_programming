#include <stdio.h>

/**
 * main - prints lowercase,uppercase alphabet
 * using putchar three times only
 *
 * Return: (0) - Success
 */
int main(void)
{
	char letter;

	letter = 'a';
	while (letter <= 'z')
	{
		putchar(letter);
		letter++;
	}
	letter = 'A';
	while (letter <= 'Z')
	{
		putchar(letter);
		letter++;
	}
	putchar('\n');
	return (0);
}
